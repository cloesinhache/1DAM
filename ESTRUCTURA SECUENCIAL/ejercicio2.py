#ENUNCIADO:Calcular el perímetro y área de un rectángulo dada su base y su altura

base=int(input("Introduzca la base del rectángulo"))
altura=int(input("Introduzca la altura del triangulo"))
perimetro=int(base*2+altura*2)
area=int((base*altura))
print("El perímetro es",perimetro)
print("El área es", area)