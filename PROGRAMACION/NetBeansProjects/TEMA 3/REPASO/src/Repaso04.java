/*ENUNCIADO:Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando
un bucle for.*/

/**
 *
 * @author cloer
 */
public class Repaso04 {
    
    public static void main(String[] args) {
        

        //bucle for
        for (int i = 320; i >= 160; i+=-20) {
            System.out.println(i);
            
        }
        
    }
    
}
